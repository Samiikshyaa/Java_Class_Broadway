package abstraction;

public class AbstractDocs {
/*
 * =================Abstraction================================
 * => process of hiding implementation details in the program
 * 
 *  to achieve abstraction:
 * - use Abstract class:
 * 		class which contains abstract methods(unimplemented method) and non abstract(implemented) methods
 *                    ***Abstract method:
 *                    			method which does not have body/implementation details.
 *                    eg:
 *                    	abstract void add();
 *                    	abstract int get Area(int l, int b); 
 *                    
 *             --> we cant create object of abstract class. 
 *             --> every abstract class must have at least one child class and child class
 *             		must override abstract methods.
 *             --> level of abstraction 0-100%
 *       
 *       Syntax:
 *        
 *        class Class_name{
 *        		//abstract metthods
 *        			eg: abstract void print();
 *        		// non- abstract methods
 *        			eg: void sum(){}
 *        }
 *                   
 * - use interface
 * 			*same as class which contains public abstract methods and public static final variables
 * 			*we can't create object of interface
 * 			*every interface must have at least one implementation class and implementation class
 * 			 must override abstract methods
 * 			* level of abstraction- 100%
 * 
 * Syntax:
 * 		interface interface_name{
 * 		
 * 			//public static final variables(ie constants)
 * 			// public abstract methods
 * 			
 * 			}
 */
}
